<script type="text/javascript">
    $( document ).on( "ready", function(e) {

        /*$( "#from_personal_info" ).on( "submit", function(e) {
            e.preventDefault();
        });*/

        var xte = function () {
          return{
              init: function () {
                  var route = "{{ route('user.profile.update', ['id' => Auth::id()]) }}";
                  var type = 'POST';
                  var async = async || false;

                  var formData = new FormData();
                  formData.append('name', $('input:text[name="name"]').val());

                  $.ajax({
                      url: route,
                      headers: { 'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content') },
                      cache: false,
                      type: type,
                      contentType: false,
                      data: formData,
                      processData: false,
                      async: async,
                      beforeSend: function () {

                      },
                      success: function (response, xhr, request) {
                          if (request.status === 200 && xhr === 'success') {
                              UIToastr.init(xhr , response.title , response.message  );
                          }
                      },
                      error: function (response, xhr, request) {
                          if (request.status === 422 &&  xhr === 'success') {
                              UIToastr.init(xhr, response.title, response.message);
                          }
                      }
                  });
              }
          }
        };


        var form = $('#from_personal_info');
        var rules = {
            name: {minlength: 5, required: true},
        };
        FormValidationMd.init(form,rules,false,xte());

    });
</script>
